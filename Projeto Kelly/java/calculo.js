function calcular(){
    var valor1 = parseInt(document.getElementById('num1').value, 10);
    var valor2 = parseInt(document.getElementById('num2').value, 10);
    var valor3 = parseInt(document.getElementById('num3').value, 10);
    var resuldado = document.getElementById('result').value = valor1 * valor2 * valor3;
    
    if (resuldado >= 100) {
        document.getElementById('ordem').value =  "1";  
    }   
    else if (resuldado < 100 && resuldado >=75 ) {
        document.getElementById('ordem').value =  "2";
      }
      else if (resuldado < 75 && resuldado >=50) {
        document.getElementById('ordem').value =  "3";
      }
      else if (resuldado < 50 && resuldado >=25) {
        document.getElementById('ordem').value =  "4";
      }
      
      
      else {
        document.getElementById('ordem').value =  "5";
      }
    

}





